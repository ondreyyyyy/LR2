# Функция вычисления суммы и произведения цифр числа
# Принимает число и возвращает два значения через запятую
def calculate_sum_and_product(num)
  sum = 0         # Инициализируем сумму нулем
  product = 1     # Инициализируем произведение единицей
  n = num.abs     # Работаем с абсолютным значением
  
  # Цикл пока число не станет нулевым
  while n != 0
    digit = n % 10  # Получаем последнюю цифру
    sum += digit
    product *= digit
    n /= 10
  end
  
  return sum, product  # Возвращаем два значения
end

# Основная программа
n = gets.to_i  # Считываем количество чисел

# Обработка каждого числа
(1..n).each do |i|
  number = gets.to_i  # Считываем текущее число
  
  # Получаем сумму и произведение
  sum, product = calculate_sum_and_product(number)
  
  # Выводим позицию если условие выполняется
  print "#{i} " if sum < product
end

puts  # Переводим строку
