# Функция вычисления суммы и произведения цифр числа
# Принимает:
#   num - число для обработки
# Возвращает кортеж (sum, product)
def calculate_sum_and_product(num):
    sum_digits = 0         # Инициализируем сумму нулем
    product = 1            # Инициализируем произведение единицей (1 * a = a)
    
    # Обрабатываем случай, если число 0
    if num == 0:
        return (0, 0)
    
    # Цикл пока число не станет нулевым (пока есть цифры)
    while num != 0:
        digit = num % 10   # Получаем последнюю цифру (остаток от деления на 10)
        sum_digits += digit      # Добавляем цифру к сумме
        product *= digit  # Умножаем произведение на цифру
        num = num // 10         # Удаляем последнюю цифру (целочисленное деление на 10)
    
    return (sum_digits, product)

n = int(input())           # Считываем количество чисел

# Цикл по всем числам от 1 до n
for i in range(1, n + 1):
    number = int(input())  # Считываем текущее число
    
    sum_d, product_d = calculate_sum_and_product(number)
    
    # Если сумма цифр меньше произведения
    if sum_d < product_d:
        print(i, end=' ')  # Выводим номер числа (позицию)

print()  # Переводим строку после вывода всех номеров
