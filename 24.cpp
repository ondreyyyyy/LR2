#include <iostream>  // Подключаем библиотеку для ввода/вывода

using namespace std; // Используем пространство имен std для удобства

// Функция вычисления суммы и произведения цифр числа
// Принимает:
//   num - число для обработки
//   sum - ссылка на переменную для записи суммы цифр (передается по ссылке)
//   product - ссылка на переменную для записи произведения цифр
void calculateSumAndProduct(int num, int& sum, int& product) {
    sum = 0;         // Инициализируем сумму нулем
    product = 1;     // Инициализируем произведение единицей (1 * a = a)
    
    int digit;       // Переменная для хранения текущей цифры
    
    // Цикл пока число не станет нулевым (пока есть цифры)
    while (num != 0) {
        digit = num % 10;  // Получаем последнюю цифру (остаток от деления на 10)
        sum += digit;      // Добавляем цифру к сумме
        product *= digit;  // Умножаем произведение на цифру
        num /= 10;         // Удаляем последнюю цифру (целочисленное деление на 10)
    }
}

int main() {
    int n;           // Переменная для количества чисел
    cin >> n;        // Считываем количество чисел
    
    int number;      // Переменная для хранения текущего числа
    
    // Цикл по всем числам от 1 до n
    for (int i = 1; i <= n; ++i) {
        cin >> number;  // Считываем текущее число
        
        int sum, product;  // Переменные для суммы и произведения цифр
        // Вызываем функцию для их вычисления
        calculateSumAndProduct(number, sum, product);
        
        // Если сумма цифр меньше произведения
        if (sum < product) {
            cout << i << " ";  // Выводим номер числа (позицию)
        }
    }
    
    cout << endl;  
    return 0;      
}
